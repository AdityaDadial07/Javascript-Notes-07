//Adding, removing, replacing class of elements

//Adding class of element
let heading=document.querySelector('h2');
heading.classList.add('newClass')
console.log(heading)

//Removing class of element
heading.classList.remove('newClass')
console.log(heading)

//Replacing class of elment 
heading.classList.replace('CodersDost','NewCoder')
console.log(heading)
